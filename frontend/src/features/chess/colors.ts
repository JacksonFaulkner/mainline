export function colorForArrowSlot(slot: number): string {
  if (slot === 1) return "rgba(16, 185, 129, 0.92)"
  if (slot === 2) return "rgba(59, 130, 246, 0.9)"
  if (slot === 3) return "rgba(244, 63, 94, 0.88)"
  if (slot === 4) return "rgba(234, 179, 8, 0.9)"
  if (slot === 5) return "rgba(249, 115, 22, 0.9)"
  return "rgba(168, 85, 247, 0.88)"
}
